            <div class="prose max-w-none">
                <p class="text-lg mb-8">Rotary Danmarks Hjælpefond blev stiftet den 5. juli 1969.</p>

                <p class="mb-6">Ved distriktskonferencerne i 1969 var et af generalforsamlingspunkterne 'forelæggelse af planer for Dansk Rotarys Hjælpearbejde'. Der var forud for generalforsamlingen udsendt bemærkning fra guvernørerne lydende: 'Et af flere klubber fremsat ønske om etablering af en fælles-dansk Rotary-fond er behandlet adskillige gange af Rotary Danmarks Råd. Den endelige fundats foreligger nu og fremlægges hermed, idet det bedes bemærket, at evt. bidrag til denne fond vil være en sag, hvortil de enkelte klubber og de enkelte rotarianere m.v. tager stilling år for år. Tilslutning til etablering af Dansk Rotarys Hjælpearbejde indebærer således ikke, at nogen bindes til bidrag'.</p>

                <h2 class="text-2xl font-bold text-gray-900 mt-12 mb-6">Fondens formål og ledelse</h2>
                <p class="mb-6">Helt fra begyndelsen var det fondens formål 'at yde støtte til løsning af sociale, kulturelle og andre samfundsgavnlige opgaver i Danmark og i udlandet, fortrinsvis i tilfælde, hvor opgaverne er af en sådan størrelse, at de ikke kan forudsættes løst af de enkelte klubber'.</p>

                <h2 class="text-2xl font-bold text-gray-900 mt-12 mb-6">Rotary-projekter for millioner</h2>
                <p class="mb-6">I 1984 blev det fastsat, at klubberne bidrager gennem kontingentindbetalinger, og der blev indbetalt 191.000 kr. Siden er beløbet hævet ad flere omgange, og i dag betaler alle rotarianere 160 kr. til Hjælpefonden og dermed til klubbernes projektarbejde.</p>

                <div class="bg-blue-50 border-l-4 border-blue-500 p-6 my-8">
                    <p class="text-blue-900">Klubben skal som minimum komme med samme beløb, som klubben søger om hos Hjælpefonden, og flere klubber søger desuden District Grant eller Global Grant, således at Rotaryklubbernes projektarbejde årligt når op på en værdi af ca. 5 mio. kr.</p>
                </div>

                <p class="mb-6">Det er interessant at se, at projekterne tidligere og nu ikke er så forskellige endda. Katastrofer øger altid lysten til at hjælpe. Dengang blev der givet store beløb til forskellige katastrofer rundt om i verden.</p>

                <p class="mb-6">Der lægges vægt på, at klubberne er aktive ved gennemførelsen af projekter – allerhelst med det praktiske arbejde, men som et minimum med at tilvejebringe projektets økonomi. Det har givet rotarianere uforglemmelige oplevelser, og det er med til at skabe en identitet og et godt sammenhold i klubberne.</p>
